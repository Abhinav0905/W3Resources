class Twinkle():
    @staticmethod
    def poem():
        return ("Twinkle, twinkle, little star,\n"
                "\t \n How I wonder what you are!\n"
                "\t \n Up above the world so high,\n"
                "\t Like a diamond in the sky.\n"
                "\t Twinkle, twinkle, little star,\n"
                "\t How I wonder what you are!\n"
                "\t When the blazing sun is gone,\n"
                "\t When he nothing shines upon,\n")


if __name__ == "__main__":
    a=Twinkle.poem()
    print(a)


